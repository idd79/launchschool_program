class Card
  CARDS_KEYS = ('2'..'10').to_a + %w(Jack, Queen, King, Ace)
  CARDS_VALUES = (2..14).to_a
  CARDS = CARDS_KEYS.zip(CARDS_VALUES).to_h

  attr_reader :rank, :suit, :rank_value

  include Comparable

  def initialize(rank, suit)
    @rank = rank
    @rank_value = CARDS[@rank.to_s]
    @suit = suit
  end

  def <=>(other_card)
    rank_value <=> other_card.rank_value
  end

  def to_s
    "#{rank} of #{suit}"
  end
end

